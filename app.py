import streamlit as st
from transformers import pipeline

st.set_page_config(page_title="BERT Sentiment Analyzer", page_icon="ü§ñ")
st.title("ü§ñ Sentiment Analyzer (Powered by BERT)")
st.markdown("Enter a sentence below and get a high-accuracy sentiment classification using a fine-tuned BERT model.")

# Load once (cached)
@st.cache_resource
def load_model():
    return pipeline("sentiment-analysis")

analyzer = load_model()

# User input
text = st.text_area("Write something...")

if st.button("Analyze"):
    if text:
        with st.spinner("Analyzing..."):
            result = analyzer(text)[0]
            label = result['label']
            score = result['score']

            st.subheader("üìä Result")
            st.write(f"**Sentiment:** {label}")
            st.write(f"**Confidence:** {score:.2f}")
    else:
        st.warning("‚ö†Ô∏è Please enter some text.")